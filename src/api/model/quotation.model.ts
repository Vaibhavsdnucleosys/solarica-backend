// Delete quotation
export const deleteQuotationModel = async (id: string) => {
  const quotation = await prisma.quotation.findUnique({ where: { id } });

  if (!quotation) {
    throw new Error('Quotation not found');
  }

  // Delete PDF from Supabase Storage if exists
  if (quotation.pdfFilePath) {
    await deletePDFFromSupabase(quotation.pdfFilePath, 'quotations');
  }

  // Delete extra docs if they exist
  // @ts-ignore
  if (quotation.doc1) await deletePDFFromSupabase(quotation.doc1, 'documents');
  // @ts-ignore
  if (quotation.doc2) await deletePDFFromSupabase(quotation.doc2, 'documents');
  // @ts-ignore
  if (quotation.doc3) await deletePDFFromSupabase(quotation.doc3, 'documents');


  await prisma.quotation.delete({ where: { id } });
  return { message: 'Quotation deleted successfully' };
};

// Get PDF download URL
export const getPDFDownloadURLModel = async (id: string) => {
  const quotation = await prisma.quotation.findUnique({ where: { id } });

  if (!quotation) {
    throw new Error('Quotation not found');
  }

  if (!quotation.pdfFilePath) {
    throw new Error('No PDF available for this quotation');
  }

  const signedURL = await generateSignedURL(quotation.pdfFilePath, 'quotations');
  return { url: signedURL };
};

export const generateQuotationPDFModel = async (quotationId: string, extraData: any = {}) => {
  const quotation = await prisma.quotation.findUnique({
    where: { id: quotationId },
    include: {
      createdBy: {
        select: {
          id: true,
          name: true,
          email: true
        }
      },
      items: true
    }
  });
  if (!quotation) {
    throw new Error('Quotation not found');
  }

  // Merge database fields with extra data from the request
  const combinedData = {
    ...quotation,
    ...extraData,
    // Merge items: use extraData items if available (has fresh form data), otherwise use DB items
    items: extraData.items && extraData.items.length > 0 ? extraData.items : quotation.items
  };

  // Generate PDF using template
  const pdfFilePath = await generateQuotationPDF(combinedData);

  // Update quotation with PDF path
  const updatedQuotation = await prisma.quotation.update({
    where: { id: quotationId },
    data: { pdfFilePath },
    include: {
      createdBy: {
        select: {
          id: true,
          name: true,
          email: true
        }
      },
      items: true
    }
  });
  return updatedQuotation;
};

